"""
Сервис для rule-based ответов AI-куратора.

Предоставляет быстрые ответы для типовых сценариев без обращения к AI.
"""
import re
from typing import Optional


class AIResponseService:
    """
    Сервис для rule-based ответов.
    
    Предоставляет быстрые ответы для частых сценариев:
    - Терминал сломан
    - ДТП
    - Повреждение товара
    - Недозвон
    """
    
    def build_courier_quick_reply(self, question: str) -> Optional[str]:
        """
        Построить быстрый ответ для типового сценария.
        
        Args:
            question: Вопрос курьера
            
        Returns:
            Ответ или None если не найден типовой сценарий
        """
        question_lower = question.lower()
        
        # Терминал сломан
        if any(keyword in question_lower for keyword in ["терминал", "терминал сломан", "не работает терминал", "терминал не работает"]):
            return (
                "🔧 <b>Терминал не работает</b>\n\n"
                "Если терминал не работает:\n"
                "1. Попробуйте перезагрузить терминал\n"
                "2. Проверьте подключение к интернету\n"
                "3. Если проблема не решена — обратитесь к куратору\n\n"
                "В случае проблем с оплатой — используйте резервный способ оплаты."
            )
        
        # ДТП
        if any(keyword in question_lower for keyword in ["дтп", "авария", "столкновение", "попал в аварию"]):
            return (
                "🚨 <b>ДТП</b>\n\n"
                "При ДТП:\n"
                "1. Обеспечьте безопасность себя и окружающих\n"
                "2. Вызовите ГИБДД\n"
                "3. Немедленно свяжитесь с куратором\n"
                "4. Сфотографируйте место происшествия\n\n"
                "⚠️ <b>Важно:</b> Не покидайте место ДТП до приезда сотрудников ГИБДД."
            )
        
        # Повреждение товара
        if any(keyword in question_lower for keyword in ["поврежден", "разбит", "сломан товар", "товар поврежден"]):
            return (
                "📦 <b>Повреждение товара</b>\n\n"
                "Если товар поврежден:\n"
                "1. Сфотографируйте повреждение\n"
                "2. Свяжитесь с клиентом\n"
                "3. Предложите замену или возврат\n"
                "4. При необходимости обратитесь к куратору\n\n"
                "Все повреждения должны быть задокументированы."
            )
        
        # Недозвон
        if any(keyword in question_lower for keyword in ["недозвон", "не дозвонился", "не отвечает", "не могу дозвониться"]):
            return (
                "📞 <b>Недозвон</b>\n\n"
                "Если не можете дозвониться до клиента:\n"
                "1. Попробуйте позвонить еще раз через 5-10 минут\n"
                "2. Отправьте SMS с информацией о доставке\n"
                "3. Если клиент не отвечает — оставьте заказ в безопасном месте (если возможно)\n"
                "4. При невозможности доставки — свяжитесь с куратором\n\n"
                "Все попытки связи должны быть задокументированы."
            )
        
        return None
    
    def build_courier_generic_answer(self, question: str) -> str:
        """
        Построить общий ответ для вопроса курьера.
        
        Используется когда AI недоступен или нет релевантных FAQ.
        
        Args:
            question: Вопрос курьера
            
        Returns:
            Общий ответ
        """
        return (
            "👋 Спасибо за ваш вопрос!\n\n"
            "Я обрабатываю ваш запрос. Если вопрос требует срочного решения, "
            "рекомендую обратиться к куратору напрямую.\n\n"
            "Для получения более точного ответа убедитесь, что:\n"
            "• Вопрос сформулирован конкретно\n"
            "• Указаны все важные детали\n"
            "• При необходимости приложены фотографии"
        )


# Глобальный экземпляр сервиса
ai_response_service = AIResponseService()
