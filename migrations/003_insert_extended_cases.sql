-- Выводим статистику
DO $$
DECLARE
    total_count INTEGER;
    by_category RECORD;
BEGIN
    SELECT COUNT(*) INTO total_count FROM faq_ai;
    RAISE NOTICE 'Всего FAQ в базе: %', total_count;
    
    FOR by_category IN 
        SELECT category, COUNT(*) as cnt 
        FROM faq_ai 
        WHERE category IS NOT NULL
        GROUP BY category 
        ORDER BY cnt DESC
    LOOP
        RAISE NOTICE '  %: % записей', by_category.category, by_category.cnt;
    END LOOP;
END $$;
